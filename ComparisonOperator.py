"""
< : less than
> : Greter than
<= : less than or equal to
>= : Greater than or equal to
== : Equal to
!= : Not Equal to
"""
x = 5
y = 2
print("less than ", x<y)
print("Grater than" , x>y)
print('Less than or eqal to' , x<=y)
print('Greater than or eqal to ', x>=y)
print("Equal to", x==y)
print("Not equal to ", x!=y)